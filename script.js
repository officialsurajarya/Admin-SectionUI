// Render Table
function renderTable() {
    const limit = parseInt(rowLimit.value);
    const search = searchInput.value.toLowerCase();

    filteredData = data.filter(user =>
        Object.values(user).some(value =>
            String(value).toLowerCase().includes(search)
        )
    );

    const totalPages = Math.ceil(filteredData.length / limit);
    currentPage = Math.min(currentPage, totalPages || 1);

    const startIndex = (currentPage - 1) * limit;
    const pageData = filteredData.slice(startIndex, startIndex + limit);

    tableBody.innerHTML = pageData.map(user => `
        <tr>
            <td>${user.id}</td>
            <td>${user.name}</td>
            <td>${user.email}</td>
            <td>${user.role}</td>
            <td>${user.department}</td>
            <td>${user.status}</td>
            <td>${user.country}</td>
            <td>${user.phone}</td>
            <td>${user.age}</td>
            <td>${user.joinDate}</td>
        </tr>`).join("");

    renderPagination(totalPages);
}

// Pagination
function renderPagination(totalPages) {
    paginationContainer.innerHTML = "";

    const createBtn = (label, page = null, disabled = false, active = false) => {
        const li = document.createElement("li");
        li.className = `page-item ${disabled ? "disabled" : ""} ${active ? "active" : ""}`;
        li.innerHTML = `<a class="page-link" href="#">${label}</a>`;
        if (!disabled && page) {
            li.addEventListener("click", e => {
                e.preventDefault();
                currentPage = page;
                renderTable();
            });
        }
        return li;
    };

    paginationContainer.appendChild(createBtn("Â«", currentPage - 1, currentPage === 1));

    for (let i = 1; i <= totalPages; i++) {
        if (i === 1 || i === totalPages || Math.abs(currentPage - i) <= 1) {
            paginationContainer.appendChild(createBtn(i, i, false, currentPage === i));
        } else if (
            (i === 2 && currentPage > 3) ||
            (i === totalPages - 1 && currentPage < totalPages - 2)
        ) {
            const ellipsis = document.createElement("li");
            ellipsis.className = "page-item disabled";
            ellipsis.innerHTML = `<span class="page-link">...</span>`;
            paginationContainer.appendChild(ellipsis);
        }
    }

    paginationContainer.appendChild(createBtn("Â»", currentPage + 1, currentPage === totalPages));
}

